#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	if (!lst || !(*lst) || !del)
		return ;
	del((*lst)->content);
	ft_lstclear(&(*lst)->next, del);
	free(*lst);
	*lst = NULL;
}
