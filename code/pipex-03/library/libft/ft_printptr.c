#include "libft.h"

int	ft_printptr(void *add)
{
	if (!add)
	{
		write(1, "(nil)", 5);
		return (5);
	}
	write(1, "0x", 2);
	return (2 + ft_prptr((unsigned long) add, "0123456789abcdef", 16));
}
